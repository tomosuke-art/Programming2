// アスキーコード表を書く
int main(){
  char a = ' ', i;
  printf(" ********* ASCII CODE TABLE ********\n");
  for (i = 0;i < 96;++i){
    printf(" %c=%2x", a + i, a + i);
    if ((i % 8) == 7){
     printf("\n");
    }
  }
}

